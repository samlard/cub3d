#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	lenght;
	char	*str;

	if (!s || !f)
		return (NULL);
	lenght = ft_strlen(s);
	str = malloc(sizeof(char) * (lenght + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
